/**
 * NetworkAddress types
 */
enum addr_family_e {
  NET_ADDR_FAM_ALG          = AF_ALG,         // Algorithm sockets (cryptographic)
  NET_ADDR_FAM_APPLETALK    = AF_APPLETALK,   // AppleTalk protocol
  NET_ADDR_FAM_ASH          = AF_ASH,         // Ash
  NET_ADDR_FAM_ATMPVC       = AF_ATMPVC,      // ATM PVCs
  NET_ADDR_FAM_ATMSVC       = AF_ATMSVC,      // ATM SVCs
  NET_ADDR_FAM_AX25         = AF_AX25,        // Amateur radio AX.25 protocol
  NET_ADDR_FAM_BLUETOOTH    = AF_BLUETOOTH,   // Bluetooth
  NET_ADDR_FAM_BRIDGE       = AF_BRIDGE,      // Multiprotocol bridge
  NET_ADDR_FAM_CAIF         = AF_CAIF,        // CAIF sockets, for cellular modem communication
  NET_ADDR_FAM_CAN          = AF_CAN,         // Controller Area Network
  NET_ADDR_FAM_DECnet       = AF_DECnet,      // DECnet
  NET_ADDR_FAM_ECONET       = AF_ECONET,      // Acorn Econet
  NET_ADDR_FAM_FILE         = AF_FILE,        // Synonym for AF_UNIX
  NET_ADDR_FAM_IB           = AF_IB,          // InfiniBand
  NET_ADDR_FAM_IEEE802154   = AF_IEEE802154,  // IEEE 802.15.4 sockets, low-rate wireless personal area networks
  NET_ADDR_FAM_INET         = AF_INET,        // IPv4 Internet protocols
  NET_ADDR_FAM_INET6        = AF_INET6,       // IPv6 Internet protocols
  NET_ADDR_FAM_IPX          = AF_IPX,         // Novell Internet Packet Exchange
  NET_ADDR_FAM_IRDA         = AF_IRDA,        // IRDA sockets
  NET_ADDR_FAM_ISDN         = AF_ISDN,        // ISDN
  NET_ADDR_FAM_IUCV         = AF_IUCV,        // Inter-user communication vehicle
  NET_ADDR_FAM_KCM          = AF_KCM,         // Kernel connection multiplexor
  NET_ADDR_FAM_KEY          = AF_KEY,         // Key management protocol
  NET_ADDR_FAM_LLC          = AF_LLC,         // Logical Link Control
  NET_ADDR_FAM_LOCAL        = AF_LOCAL,       // Local communication (UNIX sockets)
  NET_ADDR_FAM_MAX          = AF_MAX,         // Placeholder for the maximum value
  NET_ADDR_FAM_MCTP         = AF_MCTP,        // Management component transport protocol
  NET_ADDR_FAM_MPLS         = AF_MPLS,        // MPLS
  NET_ADDR_FAM_NFC          = AF_NFC,         // Near Field Communication
  NET_ADDR_FAM_NETBEUI      = AF_NETBEUI,     // NETBEUI protocol
  NET_ADDR_FAM_NETLINK      = AF_NETLINK,     // Kernel user interface device
  NET_ADDR_FAM_NETROM       = AF_NETROM,      // Amateur Radio NET/ROM
  NET_ADDR_FAM_PACKET       = AF_PACKET,      // Low-level packet interface
  NET_ADDR_FAM_PHONET       = AF_PHONET,      // Phonet protocols
  NET_ADDR_FAM_PPPOX        = AF_PPPOX,       // PPP over X
  NET_ADDR_FAM_QIPCRTR      = AF_QIPCRTR,     // Qualcomm IPC router
  NET_ADDR_FAM_RDS          = AF_RDS,         // Reliable Datagram Sockets
  NET_ADDR_FAM_ROSE         = AF_ROSE,        // Amateur radio X.25 PLP
  NET_ADDR_FAM_ROUTE        = AF_ROUTE,       // Alias to facilitate routing
  NET_ADDR_FAM_RXRPC        = AF_RXRPC,       // RxRPC sockets
  NET_ADDR_FAM_SECURITY     = AF_SECURITY,    // Security hooks
  NET_ADDR_FAM_SMC          = AF_SMC,         // Shared memory communications
  NET_ADDR_FAM_SNA          = AF_SNA,         // IBM SNA
  NET_ADDR_FAM_TIPC         = AF_TIPC,        // TIPC
  NET_ADDR_FAM_UNIX         = AF_UNIX,        // UNIX domain sockets
  NET_ADDR_FAM_UNSPEC       = AF_UNSPEC,      // Unspecified
  NET_ADDR_FAM_VSOCK        = AF_VSOCK,       // VMCI Sockets
  NET_ADDR_FAM_WANPIPE      = AF_WANPIPE,     // Wanpipe
  NET_ADDR_FAM_X25          = AF_X25,         // ITU-T X.25 / ISO-8208 protocol
  NET_ADDR_FAM_XDP          = AF_XDP,         // Express Data Path
  NET_ADDR_FAM_UNKNOWN      = -1              // Reserved for error indications
};



